// if statement

i = 4
if(i < 5) {
    println("i is less than 5")    
}

// if else statement

i = 6
if(i < 5) {
    println("i is less than 5")
    } else {
    println("i is more than 5")  
}

// else if statement

i = 11
if(i < 5) {
    println("i is less than 5")
    } else if (i < 10) {
    println("i is less than 10")
    } else {
    println("i is more than 10")      
}

// switch

i = 4
switch(i) {
   case 5:    
     println("i je 5")
     break;
   case 4:    
     println("i je 4")
     break;
    default:
     println("i je neco jineho")
}